def realizar_suma(num1 , num2):
  resultado = num1 + num2 
  print(resultado)  
realizar_suma(40 , 60)

def realizar_resta(num1, num2):
  resultado = num1 - num2
  print(resultado)
realizar_resta(100 , 50)

def realizar_multiplicacion(num1 , num2):
  resultado = num1 * num2
  print(resultado)
realizar_multiplicacion(5 , 10) 

def realizar_division(num1 , num2):
  return num1 / num2 ;
print(realizar_division(20 , 5))

